print("Hello, this will print all operations using the two input numbers!")
#get two numbers from the user
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

#displaying the sum of  the two numbers
print("The sum of that two numbers is", num1 + num2)

#displaying the difference of two numbers
print("The difference between that two numbers is", num1 - num2)

#displaying the product of the two numbers
print("The product of that two numbers is", num1 * num2)

#displaying the quotient of the two numbers
print("The quotient of that two numbers is", num1 / num2)

#displaying the remainder
print("And its remainder is", num1 % num2)